# Using math Module for calculation

import math

num = float(input("Enter the Number: "))

output =  math.sqrt (num)
print(f"Square root of the {num} : {output}")

output1 = math.log(num)
print (f"Logarithm of the {num} : {output1}")

output_2 = math.sin(num)
print (f"Sine of the {num} : {output_2}")